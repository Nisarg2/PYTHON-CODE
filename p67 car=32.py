light_color = input("Enter the traffic light color (green, yellow, red): ")
if light_color == "green":
        print("The light is green. The car is allowed to go.")
elif light_color == "yellow":
        print("The light is yellow. has to wait.")
elif light_color == "red":
        print("The light is red. a  car stop.")
else:
        print("Invalid light color.")


